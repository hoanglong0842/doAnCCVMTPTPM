    private void btnchonfiledpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchonfiledpfActionPerformed
        // TODO add your handling code here:
        try {
            PDDocument pdDoc;
            PDFParser parser;
            PDFTextStripper pdfStripper;
            COSDocument cosDoc;
            File file;
            JFileChooser j = new JFileChooser("D:/ABC/");
            int r = j.showOpenDialog(null);
            if (r == JFileChooser.APPROVE_OPTION) {
                String fileName = j.getSelectedFile().getAbsolutePath();
                if (fileName.endsWith(".pdf")) {
                    file = new File(fileName);
                    parser = new PDFParser(new org.apache.pdfbox.io.RandomAccessFile(file, "r"));
                    parser.parse();
                    cosDoc = parser.getDocument();
                    pdfStripper = new PDFTextStripper();
                    pdDoc = new PDDocument(cosDoc);
                    pdDoc.getNumberOfPages();
                    pdfStripper.setStartPage(0);
                    pdfStripper.setEndPage(pdDoc.getNumberOfPages());
                    txttext.setText(pdfStripper.getText(pdDoc));
                    JOptionPane.showMessageDialog(null, "Load pdf file success!");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Please choose a pdf file!");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnchonfiledpfActionPerformed

    private void btnghiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnghiActionPerformed
        // TODO add your handling code here:
        String fileName;
        JFileChooser j = new JFileChooser("D:/ABC/");
        int r = j.showSaveDialog(null);
        if (r == JFileChooser.APPROVE_OPTION) {
            fileName = j.getSelectedFile().getAbsolutePath();
            System.setProperty("freetts.voices", "com.sun.speech.freetts.en.us.cmu_us_kal.KevinVoiceDirectory");
            voice = VoiceManager.getInstance().getVoice(voiceName);
            if (voice != null) {
                AudioFileFormat.Type targettype = AudioFileFormat.Type.WAVE;
                audioPlayer = new SingleFileAudioPlayer(fileName, targettype);
                voice.allocate();// Allocating Voice
                try {
                    voice.setRate(130);// Setting the rate of the voice
                    voice.setPitch(170);// Setting the Pitch of the voice
                    voice.setVolume(3);// Setting the volume of the voice
                    voice.setAudioPlayer(audioPlayer);
                    voice.speak(txttext.getText().trim());// Calling speak() method
                    voice.deallocate();
                    audioPlayer.close();
                    JOptionPane.showMessageDialog(rootPane, "Write audio file success!");
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(rootPane, "Write audio file fail!");
                }
            } else {
                throw new IllegalStateException("Cannot find voice: kevin16");
            }
        }
    }//GEN-LAST:event_btnghiActionPerformed

