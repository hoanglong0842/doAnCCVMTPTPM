    private void btndocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndocActionPerformed
        // TODO add your handling code here:
        if (btndoc.getText().equals("SPEAK")) {
            btndoc.setIcon(new ImageIcon(new ImageIcon("src/image/playing.png").getImage().getScaledInstance(20, 20, Image.SCALE_DEFAULT)));
            btndoc.setText("STOP");
            Thread thread = new Thread() {
                public void run() {
                    System.setProperty("freetts.voices", "com.sun.speech.freetts.en.us.cmu_us_kal.KevinVoiceDirectory");
                    voice = VoiceManager.getInstance().getVoice(voiceName);
                    if (voice != null) {
                        voice.allocate();// Allocating Voice
                        try {
                            voice.setRate(115);// Setting the rate of the voice
                            voice.setPitch(135);// Setting the Pitch of the voice
                            voice.setVolume(2);// Setting the volume of the voice
                            voice.speak(txttext.getText().trim());// Calling speak() method

                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                        finally{
                            btndoc.setText("SPEAK");
                            btndoc.setIcon(new ImageIcon(new ImageIcon("src/image/speaker.png").getImage().getScaledInstance(20, 20, Image.SCALE_DEFAULT)));
                        }
                    } else {
                        throw new IllegalStateException("Cannot find voice: kevin16");
                    }
                }
            };
            thread.start();
        } else {
            voice.deallocate();
            btndoc.setText("SPEAK");
            btndoc.setIcon(new ImageIcon(new ImageIcon("src/image/speaker.png").getImage().getScaledInstance(20, 20, Image.SCALE_DEFAULT)));
        }
        
    }//GEN-LAST:event_btndocActionPerformed

